import java.util.*;

public class Solution {
	public static void main(String args[]) throws Exception {
		Scanner sc = new Scanner(System.in);

		int T = sc.nextInt();
		
		for (int tc = 1; tc <= T; tc++) {

			String s = sc.next();
			String ans = "Odd";
			if(s.charAt(s.length()-1)%2==0){
				ans = "Even";
			}
			
			
			
			
			
			
			
			
			
			
			
			System.out.println("#"+tc+" "+ans);
			
			
			
		}

	}
}
